// Пакет, в котором находится модель данных для запроса отправки SMS
package ru.contlog.mobile.helper.model

// Импорт аннотации для поддержки сериализации через kotlinx.serialization
import kotlinx.serialization.Serializable

/**
 * Модель данных, представляющая параметры запроса на отправку SMS с кодом подтверждения.
 *
 * Используется при первом шаге аутентификации: пользователь вводит номер телефона,
 * и клиент отправляет его на сервер, чтобы инициировать отправку SMS.
 *
 * Аннотация @Serializable позволяет автоматически преобразовать этот объект в JSON
 * при выполнении сетевого запроса (например, с помощью Ktor, Retrofit + kotlinx.serialization и т.п.).
 */
@Serializable
data class AuthGetSmsParams(
    /**
     * Номер телефона пользователя в формате, ожидаемом сервером.
     * Обычно это строка вида "79123456789" (без +, но с кодом страны).
     * При сериализации поле будет называться "tel", как того ожидает сервер.
     */
    val tel: String,
)

package ru.contlog.mobile.helper.model

// Импорт аннотации из библиотеки kotlinx.serialization,
// которая помечает класс как пригодный для автоматической сериализации и десериализации
// (например, при преобразовании JSON-ответа от сервера в объект Kotlin)
import kotlinx.serialization.Serializable

// Аннотация @Serializable указывает, что этот класс может быть
// автоматически сериализован в JSON и десериализован из JSON
// с использованием библиотеки kotlinx.serialization
@Serializable

// Generic (обобщённый) data-класс, представляющий стандартную структуру ответа от API.
// Параметр типа <T> позволяет использовать этот шаблон для любого типа данных в поле "data"
// (например, User, List<Division>, Product и т.д.)
data class GenericApiResponse<T> (

    // Поле "status" — логический флаг, указывающий, успешно ли выполнен запрос:
    // "true" — операция прошла успешно, "false" — произошла ошибка
    val status: Boolean,

    // Поле "error" — дополнительный флаг, явно сигнализирующий о наличии ошибки:
    // "true" — ответ содержит ошибку, "false" — ошибки нет
    // (может дублировать "status", но часто используется для совместимости с legacy-API)
    val error: Boolean,

    // Поле "message" — текстовое сообщение от сервера, предназначенное для отображения пользователю
    // или логирования (например: "Успешно", "Неверный токен", "Данные не найдены")
    val message: String,

    // Поле "data" — основная полезная нагрузка ответа.
    // Тип этого поля определяется при использовании класса (например, GenericApiResponse<User>)
    // и содержит сами данные, запрошенные клиентом
    val data: T,
)


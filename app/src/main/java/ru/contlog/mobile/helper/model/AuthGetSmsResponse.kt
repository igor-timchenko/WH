// Пакет, в котором находится модель ответа от сервера при запросе на отправку SMS
package ru.contlog.mobile.helper.model

// Импорт аннотации для поддержки десериализации JSON с помощью kotlinx.serialization
import kotlinx.serialization.Serializable

/**
 * Модель данных, представляющая ответ сервера на запрос отправки SMS с кодом подтверждения.
 *
 * Используется для обработки результата вызова эндпоинта вроде /api/auth/get-sms.
 * Содержит информацию о том, успешно ли был инициирован процесс отправки SMS.
 *
 * Аннотация @Serializable позволяет автоматически преобразовать JSON-ответ от сервера
 * в объект этого класса.
 */
@Serializable
data class AuthGetSmsResponse(
    /**
     * Указывает, был ли запрос обработан успешно.
     * Обычно true — успех, false — ошибка.
     * Однако логика может зависеть от конкретной реализации сервера.
     */
    val status: Boolean,

    /**
     * Флаг, явно указывающий на наличие ошибки.
     * Часто используется как дополнительный признак: если error = true, то что-то пошло не так.
     * Может дублировать информацию из `status`, но иногда сервер использует оба поля
     * для более гибкой обработки (например, частичный успех).
     */
    val error: Boolean,

    /**
     * Текстовое сообщение от сервера.
     * Может содержать:
     *   - подтверждение ("SMS отправлено"),
     *   - описание ошибки ("Номер заблокирован", "Слишком частые запросы" и т.д.).
     * Это сообщение часто показывается пользователю или логируется.
     */
    val message: String,
)


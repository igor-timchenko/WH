package ru.contlog.mobile.helper.model


// Импорт аннотации из библиотеки kotlinx.serialization,
// которая помечает класс как пригодный для автоматической сериализации и десериализации
// (например, при парсинге JSON-ответа от сервера)
import kotlinx.serialization.Serializable

// Аннотация @Serializable указывает, что объекты этого класса могут быть
// автоматически преобразованы из JSON в Kotlin-объект и обратно
// с использованием kotlinx.serialization (например, в Ktor, Retrofit и др.)
@Serializable

// Data-класс, представляющий структуру ответа сервера на запрос отправки SMS с кодом подтверждения
data class AuthGetSmsResponse (

    // Поле "status" — логический флаг, указывающий, успешно ли выполнен запрос:
    // "true" — операция успешна, "false" — произошла ошибка
    val status: Boolean,

    // Поле "error" — дополнительный флаг, явно сигнализирующий о наличии ошибки:
    // "true" — в ответе содержится ошибка, "false" — ошибки нет
    // (иногда дублирует "status", но может использоваться для обратной совместимости или специфики API)
    val error: Boolean,

    // Поле "message" — текстовое сообщение от сервера, предназначенное для пользователя или логирования:
    // например, "Код отправлен", "Номер не найден", "Превышен лимит запросов" и т.д.
    val message: String,
)


         *           –µ—Å–ª–∏ –∫–æ–¥ –≤–µ—Ä–Ω—ã–π –∏ —Å–µ—Ä–≤–µ—Ä –ø–æ–¥—Ç–≤–µ—Ä–¥–∏–ª –∞—É—Ç–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏—é.

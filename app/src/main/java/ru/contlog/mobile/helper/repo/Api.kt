                    // 7. –î–µ—Å–µ—Ä–∏–∞–ª–∏–∑—É–µ–º JSON –≤ –æ–±—ä–µ–∫—Ç –æ—Ç–≤–µ—Ç–∞
                    val data = jsonCoder.decodeFromString<AuthGetSmsResponse>(responseText)

                    // 8. –õ–æ–≥–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞ (–¥–ª—è –æ—Ç–ª–∞–¥–∫–∏)
                    Log.i(TAG, "getSms: message: ${data.message}")

                    // 9. –ü—Ä–æ–≤–µ—Ä—è–µ–º, –≤–µ—Ä–Ω—É–ª –ª–∏ —Å–µ—Ä–≤–µ—Ä –ª–æ–≥–∏—á–µ—Å–∫—É—é –æ—à–∏–±–∫—É (–Ω–∞–ø—Ä–∏–º–µ—Ä, "–Ω–æ–º–µ—Ä –Ω–µ –Ω–∞–π–¥–µ–Ω")
                    if (data.error) {
                        return Result.failure(ApiRequestException(
                            "getSms",
                            "–ó–∞–ø—Ä–æ—Å –≤–µ—Ä–Ω—É–ª –æ—à–∏–±–∫—É: ${response.message}",
                            "–ù–µ —É–¥–∞–ª–æ—Å—å –æ—Ç–ø—Ä–∞–≤–∏—Ç—å –°–ú–°: –ø—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ –æ—Ç–≤–µ—Ç–∞ –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞"
                        ))
                    }
                    // 10. –ï—Å–ª–∏ –≤—Å—ë –ø—Ä–æ—à–ª–æ —É—Å–ø–µ—à–Ω–æ ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —É—Å–ø–µ—Ö

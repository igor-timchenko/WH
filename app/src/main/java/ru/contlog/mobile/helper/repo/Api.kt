        suspend fun getSms(
            phoneNumber: String
        ): Result<Unit> {
            try {
                // 1. –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ: —Å–µ—Ä–∏–∞–ª–∏–∑—É–µ–º –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –≤ JSON
                val payload = jsonCoder.encodeToString(AuthGetSmsParams(phoneNumber))

                // 2. –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–µ–ª–æ POST-–∑–∞–ø—Ä–æ—Å–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ application/x-www-form-urlencoded
                val body = FormBody.Builder()
                    .add("data", payload)
                    .build()

                // 3. –°–æ–±–∏—Ä–∞–µ–º HTTP-–∑–∞–ø—Ä–æ—Å

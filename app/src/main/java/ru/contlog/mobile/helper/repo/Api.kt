         *           —Å–µ—Ä–≤–µ—Ä –≤–µ—Ä–Ω—É–ª –æ—à–∏–±–∫—É –∏–ª–∏ –ø—Ä–æ–∏–∑–æ—à–ª–∞ —Å–µ—Ç–µ–≤–∞—è –ø—Ä–æ–±–ª–µ–º–∞.
         *
         * üí° –ú–µ—Ç–æ–¥ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –æ–±–æ–±—â—ë–Ω–Ω—ã–π –æ—Ç–≤–µ—Ç [GenericApiResponse<ApiAuthNetData?>],
         * —á—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –µ–¥–∏–Ω–æ–æ–±—Ä–∞–∑–Ω–æ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å —É—Å–ø–µ—à–Ω—ã–µ –∏ –æ—à–∏–±–æ—á–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã –æ—Ç API.
         */
        suspend fun checkSms(
            phoneNumber: String,
            code: String
